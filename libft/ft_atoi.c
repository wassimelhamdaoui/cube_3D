#include"libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sing;
	int	result;

	result = 0;
	sing = 0;
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sing++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - 48);
		i++;
	}
	if ((sing % 2) != 0)
		return (result * -1);
	return (result);
}
